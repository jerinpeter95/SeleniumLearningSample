
public class SampleItem {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a!=b);	
		
		//logical operators
		
		boolean x=true;
		boolean y=false;
		System.out.println(x && y);
		System.out.println(x || y);
		System.out.println(!x);
		System.out.println(!y);
		
		int c=100;
		c+=5; //c=c+5
		System.out.println("\n\n"+c);
		}

}
